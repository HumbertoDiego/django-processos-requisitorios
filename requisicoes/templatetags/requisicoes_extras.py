register = template.Library()

@register.filter(name='split')
def split(value, key):
    """
        Returns the value turned into a list.
    """
    return value.split(key)

@register.filter(name='min_dois_digitos')
def min_dois_digitos(value):
    """
        Turns the numerical string value into a two digits string.
    """
    try:
        return "%02d"%int(value)
    except:
        return value

@register.filter(name='two_last_chars')
def two_last_chars(value):
    """
        Turns a 4 char year into 2 char year getting 2 last chars.
    """
    try:
        return value[:2]
    except:
        return value

@register.filter
def divide(value, arg):
    try:
        return int(value) / int(arg)
    except (ValueError, ZeroDivisionError):
        return None

@register.filter
def join(value, arg):
    try:
        return arg.join(value)
    except:
        return str(value)

